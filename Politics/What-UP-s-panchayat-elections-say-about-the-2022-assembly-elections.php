             <p>n Uttar Pradesh, the ruling Bharatiya Janata Party (BJP) won a sweeping victory in the district panchayat president elections held on July 3, winning 67 out of 75 seats. The Samajwadi Party (SP), which had won 63 seats in the 2015 panchayat president elections, was reduced to just five seats. Earlier, on June 27, uncontested elections were held for 22 district panchayat president seats—in these, only one candidate had been nominated for the post. Of these, BJP-supported candidates took 21 seats, while one seat was won by a SP-supported candidate. In the elections held on July 3 for the district panchayat presidents of 53 districts, the BJP won 46, and the SP, four.</p><p>Though panchayat elections are not fought on party symbols, this time, all major political parties saw them as something of a test-run for the 2022 assembly elections. To ensure a good result, the BJP had started preparations as far back as 2019, posting regional and district in-charges to prepare the ground and giving training to booth committees. The party also strictly implemented a decision not to give tickets to the kin of any minister, MLA or party functionary to remove the threat of nepotism.</p><p>However, the BJP did not get the results it expected in the district panchayat members’ election, which was conducted on May 2—though BJP-backed candidates won the highest total, 966 of 3,050 seats, it was short of expectations. After this, BJP the made it a goal to win more than 65 seats in the district panchayat president elections. BJP spokesperson Sameer Singh says, “The BJP strategy, which was decided according to local caste equations in each district, was successful. For this, state in-charge Radha Mohan Singh, state president Swatantradev Singh and state general secretary Sunil Bansal visited the entire state twice.” However, SP’s chief spokesperson Rajendra Choudhary says, “The BJP government has put pressure on the district panchayat members with the help of government machinery and forced them to vote in the BJP’s favour. It is like killing democracy.”</p>
           
